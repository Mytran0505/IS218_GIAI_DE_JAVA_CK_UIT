CREATE TABLE DOIBONG (
    MAD NUMBER CONSTRAINT PK_DB PRIMARY KEY,
    TENDOI VARCHAR2(200),
    QUOCGIA VARCHAR2(200)
);

CREATE TABLE GIAIDAU(
    MAG NUMBER CONSTRAINT PK_GD PRIMARY KEY,
    TENGIAI VARCHAR2(200),
    NGAYBATDAU DATE,
    NGAYKETTHUC DATE
);

CREATE TABLE CT_GD(
    MAG NUMBER,
    MAD NUMBER,
    CONSTRAINT PK_CTGD PRIMARY KEY (MAG, MAD),
    CONSTRAINT FK_CTGD_GD FOREIGN KEY(MAG) REFERENCES GIAIDAU(MAG),
    CONSTRAINT FK_CTGD_DB FOREIGN KEY(MAD) REFERENCES DOIBONG(MAD)
);

CREATE TABLE TRANDAU(
    MAT NUMBER CONSTRAINT PK_TD PRIMARY KEY,
    NGAYDA DATE,
    DIADIEM VARCHAR2(200),
    MAG NUMBER,
    MADN NUMBER,
    MADK NUMBER,
    TISO VARCHAR2(200),
    CONSTRAINT FK_TD_DB1 FOREIGN KEY(MADN) REFERENCES DOIBONG(MAD),
    CONSTRAINT FK_TD_DB2 FOREIGN KEY(MADK) REFERENCES DOIBONG(MAD)
);


