CREATE TABLE NHACC (
    MANCC NUMBER CONSTRAINT PK_NCC PRIMARY KEY,
    TENNCC VARCHAR2(200),
    DIACHI VARCHAR2(200),
    DTHOAI VARCHAR2(200),
    EMAIL VARCHAR2(200)
);

CREATE TABLE SANPHAM(
    MASP NUMBER CONSTRAINT PK_SP PRIMARY KEY,
    TENSP VARCHAR2(200),
    SOLUONG NUMBER,
    DONGIA NUMBER,
    MANCC NUMBER,
    CONSTRAINT FK_SP_NCC FOREIGN KEY(MANCC) REFERENCES NHACC(MANCC)
);

CREATE TABLE PHIEUNHAP(
    MAPN NUMBER CONSTRAINT PK_PN PRIMARY KEY,
    NGAYLAP DATE,
    GHICHU VARCHAR2(200),
    TONGTIEN NUMBER
);

CREATE TABLE CT_NHAP(
    MAPN NUMBER,
    MASP NUMBER,
    SL NUMBER,
    CONSTRAINT FK_CTN_PN FOREIGN KEY(MAPN) REFERENCES PHIEUNHAP(MAPN),
    CONSTRAINT FK_CTN_SP FOREIGN KEY(MASP) REFERENCES SANPHAM(MASP)
);


